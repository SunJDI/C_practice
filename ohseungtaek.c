#include <stdio.h>
int main(void)
{
	int a;
	int b;
	printf("수입력");
	scanf("%d",&a);
	scanf("%d",&b);
	printf("%d-%d=%d\n",a,b,a-b);
	printf("%d+%d=%d\n",a,b,a+b);
	printf("%d*%d=%d\n",a,b,a*b);
	printf("%d/%d=%d\n",a,b,a/b);
}
